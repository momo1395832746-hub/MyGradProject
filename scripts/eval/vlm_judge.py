import os
import json
import time
import argparse
import dashscope
from tqdm import tqdm
import shutil
import uuid

# 1. 检查环境变量
if "DASHSCOPE_API_KEY" not in os.environ:
    print(" 错误：未检测到 DASHSCOPE_API_KEY 环境变量！")
    exit(1)

dashscope.api_key = os.environ["DASHSCOPE_API_KEY"]

# 2. 核心 Prompt 模板 (与我们手动测试的完全一致)
JUDGE_PROMPT_TEMPLATE = """
You are an expert computer vision reviewer for top-tier academic conferences. 
I have uploaded two images:
- Image 1 (First): The original source image.
- Image 2 (Second): The edited image generated by an AI model.

The editing intent is described below:
{edit_intent}

Evaluate the editing performance strictly on a scale of 1 to 5 for the following three metrics:

1. EQ (Editing Quality): Did the model successfully execute the instruction? (1=Failed/Ignored, 5=Perfectly executed).
2. OPP (Object-Properties Preserved): Did the model preserve the identity and intrinsic properties of the main objects that were NOT supposed to be edited? (1=Severe distortion/identity loss, 5=Perfect preservation).
3. ICP (Image-Context Preserved): Did the model keep the background and unrelated context intact without introducing artifacts? (1=Background totally messed up, 5=Pixel-perfect background preservation).

CRITICAL SCORING GUIDELINES (1-5 Scale):
Do NOT default to 1 just because there is a minor flaw. Use the full 1-5 scale based on these strict definitions:

For EQ (Editing Quality - Task Execution):
- 1: Completely failed. The target object/effect is missing or utterly unrecognizable.
- 2: Poor. The edit was attempted but is severely distorted, misplaced, or incorrect in quantity.
- 3: Acceptable. The edit is recognizable and mostly correct, but lacks realism, has some blending issues, or slight quantity discrepancies.
- 4: Good. The edit is successful, visually plausible, but might have very minor stylistic mismatches.
- 5: Perfect. Flawless execution of the instruction with perfect realism.

For OPP & ICP (Preservation of Objects & Background):
- 1: Complete destruction. The background or other objects are entirely corrupted, replaced, or heavily distorted.
- 2: Severe changes. Significant parts of the unedited regions were modified or heavily blurred.
- 3: Moderate changes. Core background structures remain, but there are noticeable artifacts, color shifts, or slight distortions.
- 4: Minor artifacts. Background is almost perfectly preserved, with only tiny, negligible noise or edge blending artifacts.
- 5: Pixel-perfect preservation. No unintended changes whatsoever.


Output ONLY a valid JSON object in the exact format below. Do NOT output any conversational text, markdown formatting blocks, or extra explanations outside the JSON:
{{
  "EQ": 0,
  "OPP": 0,
  "ICP": 0,
  "Reasoning": "Provide a very brief 1-sentence explanation here."
}}
"""

def call_vlm_judge(source_img_path, gen_img_path, edit_intent, max_retries=3):
    """调用 Qwen3.5-Plus 充当裁判"""
    prompt = JUDGE_PROMPT_TEMPLATE.format(edit_intent=edit_intent)
    
    #  核心修复：生成带 UUID 的独立临时路径，彻底杜绝 OSS 同名冲突
    temp_source_path = f"/tmp/src_{uuid.uuid4().hex}_{os.path.basename(source_img_path)}"
    temp_gen_path = f"/tmp/gen_{uuid.uuid4().hex}_{os.path.basename(gen_img_path)}"
    
    # 复制为临时文件
    shutil.copy(source_img_path, temp_source_path)
    shutil.copy(gen_img_path, temp_gen_path)
    
    messages = [
        {
            "role": "system",
            "content": [{"text": "You are a merciless and strictly objective computer vision reviewer. You NEVER hallucinate. You only judge based on the exact pixels visible in the image. If an object is not clearly there or an effect is blurred/failed, you must explicitly state it."}]
        },
        {
            "role": "user",
            "content": [
                {"image": f"file://{temp_source_path}"},
                {"image": f"file://{temp_gen_path}"},
                {"text": prompt} # 这里依然用你加了 Visual_Analysis 的 CoT 提示词
            ]
        }
    ]

    attempt = 0
    result_dict = {"EQ": 0, "OPP": 0, "ICP": 0, "Reasoning": "评测失败，达到最大重试次数。"}
    
    while attempt < max_retries:
        try:
            response = dashscope.MultiModalConversation.call(
                model="qwen3.5-plus-2026-04-20",
                messages=messages,
                result_format='message',
                temperature=0.01,
                top_p=0.01,
            )
            
            if response.status_code == 200:
                raw_content = response.output.choices[0].message.content[0]['text']
                # 清洗 Markdown 标记
                clean_json_str = raw_content.replace('```json', '').replace('```', '').strip()
                result_dict = json.loads(clean_json_str)
                break  # 成功拿到并解析出数据，跳出循环
            else:
                raise Exception(f"API Error: {response.code} - {response.message}")
                
        except Exception as e:
            attempt += 1
            wait_time = 2 ** attempt
            tqdm.write(f"  [警告] 裁判开小差了 ({e})，等待 {wait_time} 秒后重试...")
            time.sleep(wait_time)
            
    # 环保收尾：清理临时文件，不给 AutoDL 磁盘留垃圾
    if os.path.exists(temp_source_path):
        os.remove(temp_source_path)
    if os.path.exists(temp_gen_path):
        os.remove(temp_gen_path)
            
    return result_dict

def main():
    parser = argparse.ArgumentParser(description="VLM 多模态大模型裁判打分脚本")
    parser.add_argument("--dataset", type=str, default="Hybrid-EditBench.json", help="评测数据集")
    parser.add_argument("--model_name", type=str, required=True, help="要评测的模型名称 (如 ip2p, qwen)")
    args = parser.parse_args()

    # 路径动态计算
    project_root = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    dataset_path = os.path.join(project_root, "datasets", args.dataset)
    gen_dir = os.path.join(project_root, "datasets", "generated", args.model_name)
    
    # 评测结果保存路径
    eval_out_dir = os.path.join(project_root, "datasets", "results", "vl_results")
    os.makedirs(eval_out_dir, exist_ok=True)
    out_json_path = os.path.join(eval_out_dir, f"vlm_scores_{args.model_name}.json")

    # 载入或初始化结果字典 (断点续传核心)
    if os.path.exists(out_json_path):
        with open(out_json_path, 'r', encoding='utf-8') as f:
            vlm_scores = json.load(f)
        print(f" 发现历史评分记录，已加载 {len(vlm_scores)} 条，准备断点续传...")
    else:
        vlm_scores = {}

    with open(dataset_path, "r", encoding="utf-8") as f:
        dataset = json.load(f)

    print(f"\n [Qwen3.5-Plus 裁判] 开始对 {args.model_name} 进行三维盲测...")
    
    for comp, categories in dataset.items():
        for category, items in categories.items():
            for item in tqdm(items, desc=f"评测中: {comp}/{category}"):
                image_id = item["id"]
                
                # 如果这张图已经打过分了，直接跳过！
                unique_key = f"{comp}_{category}_{image_id}"
                if unique_key in vlm_scores and vlm_scores[unique_key].get("EQ", 0) > 0:
                    continue
                
                source_path = os.path.join(project_root, item["image_path"])
                gen_path = os.path.join(gen_dir, comp, category, f"{image_id}.jpg")

                if not os.path.exists(gen_path):
                    tqdm.write(f"找不到生成图像 {gen_path}，跳过该项。")
                    continue
                    
               # 假设你的外层循环或配置中有一个变量记录了当前模型，比如 model_name
                if args.model_name in ["p2pz"]:
                    # 针对 P2P 模型，提取源提示词和目标提示词
                    source_p = item.get('source_prompt', '')
                    target_p = item.get('target_prompt', '')
                    edit_intent = f"Source Prompt: '{source_p}'\nTarget Prompt: '{target_p}'"
                    
                else:
                    # 针对 IP2P 以及其他指令驱动模型，统一提取指令
                    instruction_text = item.get('instruction', '')
                    edit_intent = f"Instruction: '{instruction_text}'"

                # 安全校验：防止数据集字段为空
                if not edit_intent.strip() or edit_intent == "Instruction: ''" or edit_intent == "Source Prompt: ''\nTarget Prompt: ''":
                    tqdm.write(f"  [警告] 图像 {image_id} 缺失有效指令数据，跳过评测。")
                    continue
                    
                # 请裁判打分
                score_dict = call_vlm_judge(
                    source_img_path=source_path,
                    gen_img_path=gen_path,
                    edit_intent=edit_intent
                )
                
                # ---  修复后的加固新代码 ---
                # 1. 补全缺失的元数据：把难度级别也记录在案
                score_dict["complexity"] = comp  
                score_dict["category"] = category
                
                # 2. 构建绝对唯一的复合键，格式例如：complex_object_replacement_O_001
                unique_key = f"{comp}_{category}_{image_id}"
                
                vlm_scores[unique_key] = score_dict
                
                # 每评测完一张就立刻写入硬盘，极其安全
                with open(out_json_path, 'w', encoding='utf-8') as f:
                    json.dump(vlm_scores, f, ensure_ascii=False, indent=4)
                    
    print(f"\n 评测全部完成！裁判记录已封存在: {out_json_path}")

if __name__ == "__main__":
    main()